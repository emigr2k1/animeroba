animes_col = db.animes.find();

animes_col.map((anime) => {
    let codeName = anime.title.trim();
    codeName = codeName.replace(/[".:,;'¡!¿?*+&]/g, "");
    codeName = codeName.replace(/ /g, "-");
    codeName = codeName.toLowerCase();
    db.animes.updateOne(
        {"_id": anime._id},
        {
            $set: {"code_name": codeName}
        }
    );
});

